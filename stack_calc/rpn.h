/**
 * @file rpn.h
 * @brief Калькулятор обратной польской записи (ОПН/RPN).
 * 
 * Поддерживает:
 * - Бинарные арифметические операции (+, -, *, /, ^)
 * - Унарные операции (инверсия, смена знака)
 * - Тригонометрические и гиперболические функции
 * - Обратные тригонометрические и гиперболические функции
 * - Логарифмические функции и экспоненту
 * - Логарифм по произвольному основанию
 */

#ifndef RPN_H
#define RPN_H

/**
 * @brief Коды ошибок калькулятора.
 */
typedef enum {
    RPN_OK = 0,                    /**< Успешное вычисление */
    RPN_ERR_STACK_UNDERFLOW,       /**< Недостаток операндов в стеке */
    RPN_ERR_STACK_OVERFLOW,        /**< Избыток операндов (недостаток операций) */
    RPN_ERR_INVALID_NUMBER,        /**< Некорректный формат числа */
    RPN_ERR_INVALID_OPERATOR,      /**< Неизвестная операция или функция */
    RPN_ERR_DIVISION_BY_ZERO,      /**< Деление на ноль */
    RPN_ERR_INVALID_ARGUMENT,      /**< Недопустимый аргумент функции */
    RPN_ERR_MEMORY                 /**< Ошибка выделения памяти */
} RPNErrorCode;

/**
 * @brief Результат вычисления выражения.
 */
typedef struct {
    RPNErrorCode code;             /**< Код результата/ошибки */
    double value;                  /**< Вычисленное значение (если code == RPN_OK) */
    char message[256];             /**< Текстовое описание ошибки */
} RPNResult;

/**
 * @brief Вычислить выражение в обратной польской записи.
 * 
 * @param expression Строка с выражением ОПН (токены разделены пробелами).
 * @return RPNResult Структура с результатом или ошибкой.
 * 
 * @par Примеры использования:
 * - "2 3 +" → 5
 * - "5 2 ^" → 25 (5 в степени 2)
 * - "8 2 logb" → 3 (логарифм 8 по основанию 2)
 * 
 * @note Все тригонометрические функции принимают аргументы в радианах.
 */
RPNResult rpn_calculate(const char* expression);

/**
 * @brief Получить текстовое описание кода ошибки.
 * 
 * @param code Код ошибки.
 * @const char* Строка с описанием ошибки.
 */
const char* rpn_get_error_string(RPNErrorCode code);

#endif /* RPN_H */