/**
 * @file main.c
 * @brief Тесты для калькулятора ОПН.
 * 
 * Проверяет все пункты задания (a)-(i) и обработку ошибок.
 */

#include <stdio.h>
#include "rpn.h"

/**
 * @brief Выполнить тестовое выражение и вывести результат.
 */
static void test(const char* expression) {
    printf("Выражение: \"%s\"\n", expression);
    
    RPNResult result = rpn_calculate(expression);
    
    if (result.code == RPN_OK) {
        printf("  Результат: %g\n\n", result.value);
    } else {
        printf("  Ошибка [%d]: %s\n\n", result.code, result.message);
    }
}

int main(void) {
    printf("===========================================\n");
    printf("  КАЛЬКУЛЯТОР ОБРАТНОЙ ПОЛЬСКОЙ ЗАПИСИ\n");
    printf("===========================================\n\n");
    
    printf("--- ПУНКТ (a): Бинарные операции ---\n");
    test("2 3 +");           /* 5 */
    test("10 4 -");          /* 6 */
    test("3 4 *");           /* 12 */
    test("15 3 /");          /* 5 */
    test("2 10 ^");          /* 1024 (2^10) */
    test("2 3 4 * +");       /* 14 (2 + 3*4) */
    test("5 2 ^ 3 *");       /* 75 (5^2 * 3) */
    
    printf("--- ПУНКТ (b): Унарные операции ---\n");
    test("5 inv");           /* 0.2 (1/5) */
    test("4 ~");             /* 0.25 (1/4) */
    test("7 neg");           /* -7 */
    test("10 ±");            /* -10 */
    test("2 3 + neg");       /* -5 */
    
    printf("--- ПУНКТ (c): Тригонометрические функции ---\n");
    test("0 sin");           /* 0 */
    test("0 cos");           /* 1 */
    test("0 tan");           /* 0 */
    test("1.5707963267948966 cos");  /* ~0 (PI/2) */
    test("0.7853981633974483 tan");  /* ~1 (PI/4) */
    
    printf("--- ПУНКТ (d): Гиперболические функции ---\n");
    test("0 sinh");          /* 0 */
    test("0 cosh");          /* 1 */
    test("0 tanh");          /* 0 */
    test("1 sinh");          /* 1.175... */
    test("1 cosh");          /* 1.543... */
    
    printf("--- ПУНКТ (e): Обратные тригонометрические ---\n");
    test("0 asin");          /* 0 */
    test("1 acos");          /* 0 */
    test("0 atan");          /* 0 */
    test("0.5 asin");        /* 0.523... (PI/6) */
    test("1 atan");          /* 0.785... (PI/4) */
    
    printf("--- ПУНКТ (f): Обратные гиперболические ---\n");
    test("0 asinh");         /* 0 */
    test("1 acosh");         /* 0 */
    test("0 atanh");         /* 0 */
    test("1.5 acosh");       /* 0.962... */
    
    printf("--- ПУНКТ (g): Корни и экспонента ---\n");
    test("16 sqrt");         /* 4 */
    test("27 cbrt");         /* 3 */
    test("1 exp");           /* 2.718... (e) */
    test("0 exp");           /* 1 */
    test("2 exp");           /* 7.389... */
    
    printf("--- ПУНКТ (h): Логарифмы ---\n");
    test("2.718281828 ln");  /* ~1 */
    test("10 log");          /* 1 */
    test("100 log10");       /* 2 */
    test("1000 log10");      /* 3 */
    test("8 log2");          /* 3 */
    test("1024 log2");       /* 10 */
    test("1 ln");            /* 0 */
    
    printf("--- ПУНКТ (i): Логарифм двух аргументов ---\n");
    test("8 2 logb");        /* 3 (log_2(8) = 3) */
    test("27 3 logb");       /* 3 (log_3(27) = 3) */
    test("100 10 logb");     /* 2 (log_10(100) = 2) */
    test("16 2 logb");       /* 4 (log_2(16) = 4) */
    test("125 5 logb");      /* 3 (log_5(125) = 3) */
    test("81 3 logb");       /* 4 (log_3(81) = 4) */
    
    printf("--- ОБРАБОТКА ОШИБОК ---\n");
    printf("Недостаток операндов:\n");
    test("1 2 + +");         /* Ошибка: нужно 3 числа */
    test("5 inv inv inv");   /* Работает: 0.2 -> 5 -> 0.2 */
    
    printf("Недостаток операций:\n");
    test("1 2 3 +");         /* Ошибка: в стеке 1 и 5 */
    test("10 20");           /* Ошибка: два числа без операции */
    
    printf("Некорректный ввод:\n");
    test("1 2a +");          /* Ошибка: 2a - не число */
    test("abc");             /* Ошибка: не число */
    test("5 unknown");       /* Ошибка: неизвестная операция */
    
    printf("Деление на ноль:\n");
    test("5 0 /");           /* Ошибка */
    test("0 inv");           /* Ошибка */
    
    printf("Недопустимые аргументы:\n");
    test("2 asin");          /* Ошибка: asin определён на [-1,1] */
    test("-1 sqrt");         /* Ошибка: корень из отрицательного */
    test("0 ln");            /* Ошибка: ln(0) не определён */
    test("-5 log");          /* Ошибка: логарифм от отрицательного */
    test("1 1 logb");        /* Ошибка: основание не может быть 1 */
    test("8 0 logb");        /* Ошибка: основание должно быть > 0 */
    
    printf("===========================================\n");
    printf("  ТЕСТИРОВАНИЕ ЗАВЕРШЕНО\n");
    printf("===========================================\n");
    
    return 0;
}