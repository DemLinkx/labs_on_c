#ifndef MATRIX_H
#define MATRIX_H

#include <stddef.h>

typedef struct matrix matrix;

/**
 * Создает матрицу размером w x h
 * w - ширина
 * h - высота
 * Возвращает указатель на новую матрицу или NULL в случае ошибки
 */
matrix* m_new(size_t w, size_t h);

/**
 * Копирование матрицы
 * m - исходная матрица
 * возвращает копию матрицы или NULL в случае ошибки
 */
matrix* m_dup(const matrix* m);

/**
 * Освобождение памяти матрицы
 * m - указатель на матрицу (может быть NULL)
 */
void m_del(matrix* m);

/**
 * Получает указатель на элемент для изменения
 * m - матрица
 * i - индекс строки
 * j - индекс столбца
 * Возвращает указатель на элемент или NULL, если индексы недействительны
 */
double* m_at(matrix* m, size_t i, size_t j);

/**
 * Получает константный указатель на элемент
 * m - матрица
 * i - индекс строки
 * j - индекс столбца
 * Возвращает константный указатель на элемент или NULL, если индексы недействительны
 */
const double* m_cat(const matrix* m, size_t i, size_t j);

/**
 * Устанавливает значение элемента
 * m - матрица
 * i - индекс строки
 * j - индекс столбца
 * value - новое значение
 * возвращает 0 в случае успеха или -1 в случае ошибки (недопустимые индексы)
 */
int m_set(matrix* m, size_t i, size_t j, double value);

/**
 * Получение значения элемента
 * m - матрица
 * i - индекс строки
 * j - индекс столбца
 * value - указатель на место сохранения результата
 * Возвращает 0 в случае успеха или -1 в случае ошибки (недопустимые индексы)
 */
int m_get(const matrix* m, size_t i, size_t j, double* value);

/**
 * Получает ширину матрицы
 * m - матрица
 * возвращает количество столбцов
 */
size_t m_width(const matrix* m);

/**
 * Получает высоту матрицы
 * m - матрица
 * возвращает количество строк
 */
size_t m_height(const matrix* m);

/**
 * Обнулить все элементы
 * m - матрица
 */
void m_zero(matrix* m);

/**
 * Инициализирует матрицу как единичную матрицу
 * m - матрица (должна быть квадратной)
 * возвращает указатель на матрицу или NULL, если она не квадратная
 */
matrix* m_eye(matrix* m);

/**
 * Создает матрицу, заполненную нулями
 * w - ширина
 * h - высота
 * возвращает новую матрицу с нулями или NULL в случае ошибки
 */
matrix* m_znew(size_t w, size_t h);

/**
 * Создание единичной матрицы
 * w - размер (ширина = высота)
 * Возвращает новую единичную матрицу или NULL в случае ошибки
 */
matrix* m_enew(size_t w);

/**
 * Вывести матрицу в стандартный вывод
 * m - матрица для печати
 */
void m_print(const matrix* m);

/**
 * Чтение матрицы из стандартного ввода
 * w - ширина
 * h - высота
 * Возвращает новую матрицу с входными значениями или NULL в случае ошибки
 */
matrix* m_read(size_t w, size_t h);

/**
 * Копирует данные из одной матрицы в другую
 * m1 - целевая матрица
 * m2 - исходная матрица
 * Возвращает указатель на m1 или NULL, если размеры не совпадают
 */
matrix* m_copy_data(matrix* m1, const matrix* m2);


matrix* m_read(size_t w, size_t h);

#endif 